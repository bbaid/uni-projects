# Програма, превежда въведен текст от потребителя на пилешки език
L = list(input("Въведете текст за превод на пилешки език: "))
L = ", пи".join(L) # .join метода добавя пи и празно място пред всяка буква, ОСВЕН ПЪРВАТА
print(f"Пи{L}") # f-стринга добавя пи пред първата буква